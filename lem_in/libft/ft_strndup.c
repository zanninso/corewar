#include "libft.h"

char		*ft_strndup(const char *s1, size_t n)
{
	char	*str;

	if ((str = ft_strnew(n)))
	{
		while (n)
		{
			str[n - 1] = s1[n - 1];
			n--;
		}
	}
	return (str);
}
